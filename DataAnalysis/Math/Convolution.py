import numpy as np

# 卷积
a = [1, 2, 3, 4, 5]     # 源数组
b = [8, 7, 6]           # 卷积核数组
# 使用b作为卷积核, 对a数组执行卷积运算的运算过程如下:

#  0 0 1 2 3 4 5    最大最小对齐, 空位补零
#  6 7 8
#  对应位置相乘再相加   0 + 0 + 8
#  b数组移动
#      6 7 8
#        6 7 8
#          6 7 8
#  直至6和5对齐, 不足补零

# convolve卷积运算, 'valid'为卷积类型
c = np.convolve(a, b, 'valid')
print(c)

